#Tuple using Functions
Tuple=(10,20,40,30,50)
print(Tuple)
print("Maximum number: ", max(Tuple))
print("Minimum number: ",min(Tuple))
print("Summation of Tuple: ",sum(Tuple))
print("Sorted order: ",sorted(Tuple))
print("Length of Tuple : ",len(Tuple))
print("Reverse order of Tuple: ", tuple(reversed(Tuple)))